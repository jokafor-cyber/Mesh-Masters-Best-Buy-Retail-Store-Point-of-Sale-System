# Checking out and payment Processing
def checkout_and_pay(cart):
    if not cart:
     print("\nYour cart is empty. Please add items before checking out.")
     return
  # Calculate subtotal
    subtotal = sum(info["quantity"]*info["unit_price"] for info in cart.values())

  # Apply discount if subtotal is more than $5000
    discount = 0
    if subtotal > 5000:
       discount = subtotal * 0.05
       print("\nCongratulations! You have received a 5% discount.")

    discounted_total = subtotal - discount
    tax = discounted_total * 0.10
    total = discounted_total + tax

    # Displaying the checkout Summary
    print("\nCheckout Summary")
    print(f"Subtotal: ${subtotal:.2f}")
    print(f"Discount: ${discount:.2f}")
    print(f"Tax (10%): ${tax:.2f}")
    print(f"Total Amount Due: ${total:.2f}")

    while True:
       try:
           paid = float(input("\nEnter the amount paid by the customer: $"))
           if paid <total:
             print(f"Insufficient Funds. Please try again. Total is ${total:.2f}.")
           else:
                break
       except ValueError:
           print("Invalid input. Please enter a valid amount.")

    change = paid - total

    # Deduct purchased quantities from stock
    for product_name, info in cart.items():
        product_catalog[product_name]["stock"] -= info["quantity"]

    generate_receipt(cart, subtotal, discount, tax, total, paid, change)

    print("\nChecking stock levels.")
    for product_name in cart:
        if product_catalog[product_name]["stock"] < Low_Stock_Threshold:
           print(f" Low stock warning: {product_name} only has {product_catalog[product_name]['stock']} left!")

    print("\nTransaction completed!")

