# Adding items to the cart

def add_to_cart(cart):
    print("\nAdd Item to the Cart")

    # Asking the cashier for the product name
    product_name = input("Enter product name: ").strip().lower()

    # Searching the catalog with case-insensitive match
    matched_key = None
    for key in product_catalog:
        if product_name in key.lower(): # partial match
           matched_key = key
           break

    # Validating that the product exists in the catalog
    if matched_key is None:
        print("Product not found.")
        return

    product_name = matched_key

    # Asking the cashier for the quantity they want to add
    try:
        quantity = int(input(f"Enter quantity for '{product_name}': "))

        # Validating that the quantity is positive
        if quantity <= 0:
            print("Please enter a positive quantity.")
            return
    except ValueError:
        print("Invalid input. Please enter a number greater than 0.")
        return

    # Checking stock availability
    available_stock = product_catalog[product_name]["stock"]

    # Checking how many of this item is are already in the cart
    already_in_cart = cart.get(product_name,{}).get("quantity", 0)

    # Calculating the total amount needed (already in the cart + new request)
    total_needed = already_in_cart + quantity

    # Comparing the total needed with what is already in the cart

    if total_needed > available_stock:
      print(f"Not enough stock. Available: {available_stock}.")
      return

    # Updating item quantity that is already in the cart
    # Adding new item to the cart
    if product_name in cart:
        cart[product_name]["quantity"] += quantity
    else:
        cart[product_name] = {
           "quantity": quantity,
           "unit_price": product_catalog[product_name]["price"]
       }
    # Confirming that the item that were added
    print(f"{quantity} x '{product_name}' has been added to your cart.")

# Removing item from cart
def remove_from_cart(cart):
    if not cart:
        print(" Your cart is empty")
        return

    print("\nRemove Item from Cart:")
    print("Items in your cart:")
    for item, info in cart.items():
        print(f"{item:<25} Qty: {info['quantity']:>3}  Unit Price: ${info['unit_price']:.2f}")

    product_name= input("\nEnter the product name to remove from your cart ( or 'no' to keep items):").strip()

    if product_name.lower() == "no":
        print("\nNo items removed.")
        return

    if product_name not in cart:
        print(f"'{product_name}' is not in your cart.")
        return

    del cart[product_name]
    print(f"'{product_name}' has been removed from your cart.")

